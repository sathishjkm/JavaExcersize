package Listofallstringprograms;
//Write a Java program to convert all characters in a string to lowercase
public class Excercise2 {
    public static void main(String[] args) {
        String city = "MY native PLACE Is Papanasam";
        // Convert the above string to all lowercase.
        String lowerCase = city.toLowerCase();
        // Display the two strings for comparison.
        System.out.println("Original String: " + city);
        System.out.println("String in lowercase: " + lowerCase);
    }
}

